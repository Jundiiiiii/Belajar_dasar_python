# ELIF = else if statement 

nama = input("Nama kamu siapa? ")

if nama == "ucup": # kondisi 1
  print("Hai ucup!") # aksi true 1
elif nama == "otong": # kondisi 2
  print("eh si otong") # aksi true 2
elif nama == "budi": # kondisi 3
  print("mana bapak budi") # aksi true 3
else:
  print("au ah ga kenall sama lu") # aksi false semua
  
print("ini adalah akhir dari program")
